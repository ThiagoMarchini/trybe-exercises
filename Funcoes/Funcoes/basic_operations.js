function add(num1, num2) {
  var result = num1 + num2;
  return result;
}

function subtract(num1, num2){
  var result = num1 - num2;
  return result;
}

function divide(num1, num2){
  var result = num1 / num2;
  return result;
}

function multiply(num1, num2){
  var result = num1 * num2;
  return result;
}

function module(num1, num2){
  var result = num1 % num2;
  return result;
}